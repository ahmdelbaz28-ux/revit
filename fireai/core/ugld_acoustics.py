"""ugld_acoustics.py — Ultrasonic Gas Leak Detection (UGLD) Acoustic Physics Engine
==================================================================================
V23 — Parallel Acoustic Detection Layer

Solves the fatal Line-of-Sight limitation of optical detectors (OGI/Flame) by
modeling ultrasonic acoustic propagation from pressurized gas leaks. UGLD sensors
detect the ultrasonic noise (25 kHz - 100 kHz) generated by gas escaping an
orifice, which propagates spherically regardless of line-of-sight.

This module implements deterministic, physics-based point-to-point acoustic
propagation. Ray tracing (reflections, diffraction, obstruction shadowing)
is explicitly DEFERRED to a later phase — we lock down the free-field physics
first, as we did with Layer 5 (Flame Detector Ray Trace).

Acoustic Physics Laws (Tier 1 — Deterministic):
  1. Speed of Sound (Temperature Dependent):
       c = 331.3 * sqrt(1 + T/273.15)
     Reference: ISO 9613-1:1993 §5, ANSI S1.26-1995

  2. Spherical Divergence (Inverse Square Law):
       SPL(R) = SPL(R_ref) - 20 * log10(R / R_ref)
     Sound pressure level drops by 6 dB per doubling of distance.
     Reference: ISO 9613-1:1993 §5.2

  3. Atmospheric Attenuation (Frequency Dependent):
       Final_SPL(R) = SPL(R) - (alpha * R)
     Where alpha depends on CENTER FREQUENCY, temperature, and humidity
     per ISO 9613-1:1993 Table 2. High-frequency ultrasonic noise attenuates
     FASTER than low-frequency — this is NOT a flat coefficient.

  4. Signal-to-Noise Ratio (SNR) Trigger:
       Sensor triggers ONLY if Final_SPL >= Background_Noise + 6 dB
       AND Final_SPL >= Sensor Trigger Threshold
     Reference: IEC 61252:1993 (personal sound exposure meters),
                ISA-TR 84.00.07 (augmented safety with acoustic detection)

Architectural Integration:
  - Reads ambient temperature from EnvironmentalContext.ambient_temp_c
    to avoid Dual-Path Inconsistency (the V22 purge taught us that
    duplicate temperature fields create divergent physics paths).
  - Returns structured UGLDTriggerResult (not bool) for audit trail.
  - Uses round(value, 1) for dB — 0.1 dB is below human perception
    threshold and well within engineering precision.

Domain Separation from acoustic_calculator.py:
  acoustic_calculator.py handles NFPA 72 §18.4 audible NOTIFICATION
  (speakers, horns, 500-4000 Hz, dBA weighting).
  This module handles GAS LEAK DETECTION (25,000-100,000 Hz, dB SPL
  unweighted, ultrasonic band). These are completely separate physics
  domains with no overlap.

Standards:
  ISO 9613-1:1993   — Attenuation of sound during propagation outdoors
  ISO 9613-2:1996   — General method of calculation
  IEC 61252:1993    — Personal sound exposure meters
  ISA-TR 84.00.07   — Augmented safety with acoustic gas leak detection
  IEC 60079-29-4    — Gas detectors — performance requirements for
                       open-path (line-of-sight) and point detectors

Usage:
    from fireai.core.ugld_acoustics import (
        UltrasonicSensor, AcousticPropagation, UGLDTriggerResult,
        check_ugld_trigger, atmospheric_attenuation_db_per_m,
    )

    sensor = UltrasonicSensor(
        trigger_threshold_db=74.0,
        background_noise_db=60.0,
    )
    propagation = AcousticPropagation(
        leak_spl_at_1m=100.0,
        distance_meters=15.0,
        center_frequency_hz=40000.0,
        temp_c=40.0,
        relative_humidity_pct=50.0,
    )
    result = check_ugld_trigger(propagation, sensor)
    assert result.triggered
"""

from __future__ import annotations

