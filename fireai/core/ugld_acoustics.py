class AcousticPropagation(BaseModel):
    """Acoustic propagation model from leak source to UGLD sensor.

    Models free-field (no obstructions) spherical propagation of ultrasonic
    noise from a pressurized gas leak. The three physical effects are:

    1. Geometric Spreading (Inverse Square Law):
         SPL(R) = SPL(1m) - 20 * log10(R)
       Where R is distance in meters from the leak source.
       Assumes point source at 1m reference distance (standard for UGLD specs).

    2. Atmospheric Absorption (Frequency Dependent):
         Final_SPL(R) = SPL(R) - (alpha * R)
       Where alpha comes from ISO 9613-1:1993 and varies dramatically
       with frequency (0.12 dB/m at 25 kHz vs 5.5 dB/m at 100 kHz).

    3. Speed of Sound (Temperature Dependent):
         c = 331.3 * sqrt(1 + T/273.15)
       Used for propagation delay calculations (future Ray Tracing phase).

    The temp_c field MUST be consistent with EnvironmentalContext.ambient_temp_c
    to avoid Dual-Path Inconsistency (the V22 purge eliminated this class of
    bug for vapor density; we must not reintroduce it for acoustics).

    Reference: ISO 9613-1:1993, IEC 60079-29-4
    """

    model_config = ConfigDict(frozen=True, strict=True)

    leak_spl_at_1m: float = Field(
        ...,
        gt=0.0,
        description=(
            "Source noise level of the leak at 1 meter reference distance "
            "(dB SPL). Depends on gas pressure, orifice size, and gas type. "
            "Typical: 85-110 dB SPL at 1m for pressurized gas releases."
        ),
    )
    distance_meters: float = Field(
        ...,
        gt=0.0,
        description="Distance from leak source to UGLD sensor (meters).",
    )
    center_frequency_hz: float = Field(
        default=_DEFAULT_UGLD_FREQUENCY_HZ,
        gt=0.0,
        description=(
            "Center frequency of the ultrasonic signal (Hz). Must match "
            "the sensor's operating frequency for accurate attenuation."
        ),
    )
    temp_c: float = Field(
        default=40.0,
        ge=-40.0,
        le=85.0,
        description=(
            "Ambient temperature (C). MUST be consistent with "
            "EnvironmentalContext.ambient_temp_c to prevent Dual-Path "
            "Inconsistency. Default 40C = typical industrial indoor "
            "(matches EnvironmentalContext default)."
        ),
    )
    relative_humidity_pct: float = Field(
        default=50.0,
        ge=0.0,
        le=100.0,
        description="Relative humidity (%). Affects atmospheric attenuation.",
    )

    # ── Computed fields (set by model_validator) ──
    speed_of_sound_mps: float = Field(
        default=0.0,
        init=False,
        description="Computed speed of sound (m/s).",
    )
    geometric_loss_db: float = Field(
        default=0.0,
        init=False,
        description="Computed geometric spreading loss (dB).",
    )
    atmospheric_loss_db: float = Field(
        default=0.0,
        init=False,
        description="Computed atmospheric absorption loss (dB).",
    )
    final_spl_db: float = Field(
        default=0.0,
        init=False,
        description="Computed SPL at sensor position (dB SPL).",
    )
    alpha_db_per_m: float = Field(
        default=0.0,
        init=False,
        description="Computed atmospheric attenuation coefficient (dB/m).",
    )

    @model_validator(mode="after")
    def compute_acoustics(self) -> AcousticPropagation:
        """Compute all acoustic propagation values deterministically."""
        # 1. Temperature-dependent speed of sound (ISO 9613-1 §5)
        #    c = 331.3 * sqrt(1 + T/273.15)
        #    At 20C: c ≈ 343.2 m/s
        #    At 40C: c ≈ 354.7 m/s
        sos = 331.3 * math.sqrt(1.0 + (self.temp_c / 273.15))

        # 2. Geometric Spreading — Inverse Square Law
        #    SPL(R) = SPL(1m) - 20 * log10(R)
        #    Reference distance = 1m (standard for UGLD specifications)
        #    At R=2m: -6.02 dB (6 dB per doubling — the classic rule)
        #    At R=10m: -20.0 dB
        #    At R=100m: -40.0 dB
        geo_loss = 20.0 * math.log10(self.distance_meters)
        spl_after_geo = self.leak_spl_at_1m - geo_loss

        # 3. Atmospheric Absorption — ISO 9613-1 (frequency-dependent!)
        #    NOT a flat 0.5 dB/m — that would be physically wrong.
        #    At 40 kHz: alpha ≈ 0.5 dB/m → 15m distance = 7.5 dB loss
        #    At 100 kHz: alpha ≈ 5.5 dB/m → 15m distance = 82.5 dB loss!
        alpha = atmospheric_attenuation_db_per_m(
            center_frequency_hz=self.center_frequency_hz,
            temp_c=self.temp_c,
            relative_humidity_pct=self.relative_humidity_pct,
        )
        atm_loss = alpha * self.distance_meters

        # 4. Final SPL at sensor position
        final_spl = spl_after_geo - atm_loss

        # Set computed fields (frozen model — use object.__setattr__)
        object.__setattr__(self, "speed_of_sound_mps", round(sos, 1))
        object.__setattr__(self, "geometric_loss_db", round(geo_loss, 1))
        object.__setattr__(self, "atmospheric_loss_db", round(atm_loss, 1))
        object.__setattr__(self, "alpha_db_per_m", round(alpha, 4))
        object.__setattr__(self, "final_spl_db", round(final_spl, 1))
        return self


class UGLDTriggerResult(BaseModel):
    """Structured result of UGLD trigger check.

    Returns more than just triggered/not-triggered — provides the full
    acoustic analysis for audit trail and engineering review. This follows
    the same principle as AudibilityResult in acoustic_calculator.py:
    in a life-safety system, a boolean return is insufficient.

    Fields:
        triggered: Whether the sensor will detect the leak.
        final_spl_db: Computed SPL at the sensor position (dB SPL).
        snr_db: Signal-to-Noise Ratio (Final_SPL - Background_Noise).
        margin_to_threshold_db: Margin above trigger threshold.
        margin_to_snr_db: Margin above SNR requirement.
        fail_reason: If not triggered, which condition failed.

    Reference: ISA-TR 84.00.07, IEC 61508 SIL verification
    """

    model_config = ConfigDict(frozen=True, strict=True)

    triggered: bool
    final_spl_db: float
    background_noise_db: float
    trigger_threshold_db: float
    snr_db: float = Field(
        description="Signal-to-Noise Ratio = final_spl_db - background_noise_db",
    )
    margin_to_threshold_db: float = Field(
        description="Margin above trigger threshold = final_spl_db - trigger_threshold_db",
    )
    margin_to_snr_db: float = Field(
        description=("Margin above SNR requirement = snr_db - 6 dB minimum. Negative = insufficient SNR."),
    )
    fail_reason: Optional[str] = Field(
        default=None,
        description="If not triggered, which condition failed and why.",
    )


