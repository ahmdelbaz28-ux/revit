def check_ugld_trigger(
    propagation: AcousticPropagation,
    sensor: UltrasonicSensor,
) -> UGLDTriggerResult:
    """Check whether a UGLD sensor will detect a gas leak based on acoustic physics.

    The sensor triggers ONLY if BOTH conditions are met:
      1. Final SPL >= Sensor Trigger Threshold (hardware limit)
      2. Final SPL >= Background Noise + 6 dB (SNR requirement)

    Both conditions are AND-ed because:
      - Meeting threshold but not SNR = false alarm risk
      - Meeting SNR but not threshold = below sensor sensitivity

    Args:
        propagation: AcousticPropagation model with computed SPL at sensor.
        sensor: UltrasonicSensor model with threshold and noise specs.

    Returns:
        UGLDTriggerResult with triggered status, margins, and fail reason.

    Reference: ISA-TR 84.00.07 §4.3, IEC 61252:1993

    """
    final_spl = propagation.final_spl_db
    bg_noise = sensor.background_noise_db
    threshold = sensor.trigger_threshold_db

    snr = final_spl - bg_noise
    margin_threshold = final_spl - threshold
    margin_snr = snr - _MIN_SNR_DB

    # Both conditions must be met (AND logic)
    threshold_met = final_spl >= threshold
    snr_met = snr >= _MIN_SNR_DB
    triggered = threshold_met and snr_met

    # Determine fail reason if not triggered
    fail_reason: Optional[str] = None
    if not triggered:
        reasons = []
        if not threshold_met:
            reasons.append(
                f"Final SPL ({final_spl:.1f} dB) below trigger threshold "
                f"({threshold:.1f} dB). Deficit: {abs(margin_threshold):.1f} dB."
            )
        if not snr_met:
            reasons.append(
                f"SNR ({snr:.1f} dB) below minimum {_MIN_SNR_DB:.0f} dB. "
                f"Final SPL ({final_spl:.1f} dB) - Background ({bg_noise:.1f} dB) "
                f"= {snr:.1f} dB. Deficit: {abs(margin_snr):.1f} dB."
            )
        fail_reason = " | ".join(reasons)

    return UGLDTriggerResult(
        triggered=triggered,
        final_spl_db=final_spl,
        background_noise_db=bg_noise,
        trigger_threshold_db=threshold,
        snr_db=round(snr, 1),
        margin_to_threshold_db=round(margin_threshold, 1),
        margin_to_snr_db=round(margin_snr, 1),
        fail_reason=fail_reason,
    )


def max_detection_range_m(
    leak_spl_at_1m: float,
    sensor: UltrasonicSensor,
    temp_c: float = 40.0,
    relative_humidity_pct: float = 50.0,
) -> float:
    """Calculate the maximum detection range of a UGLD sensor for a given leak.

    Uses binary search to find the distance at which the sensor can still
    detect the leak (both threshold and SNR conditions met).

    This is the UGLD equivalent of get_speaker_coverage_radius() in
    acoustic_calculator.py — it provides the maximum effective range
    for sensor placement optimization.

    Args:
        leak_spl_at_1m: Source SPL at 1m reference distance (dB SPL).
        sensor: UltrasonicSensor specification.
        temp_c: Ambient temperature (C).
        relative_humidity_pct: Relative humidity (%).

    Returns:
        Maximum detection range in meters. 0.0 if leak cannot be detected
        even at 1m distance.

    Reference: ISA-TR 84.00.07 §4.3.2

    """
    # Quick check: can the sensor detect the leak at 1m?
    prop_1m = AcousticPropagation(
        leak_spl_at_1m=leak_spl_at_1m,
        distance_meters=1.0,
        center_frequency_hz=sensor.center_frequency_hz,
        temp_c=temp_c,
        relative_humidity_pct=relative_humidity_pct,
    )
    result_1m = check_ugld_trigger(prop_1m, sensor)
    if not result_1m.triggered:
        return 0.0

    # Binary search for maximum compliant distance
    lo, hi = 1.0, 500.0  # Search between 1m and 500m
    best_range = 1.0

    for _ in range(50):  # ~50 iterations gives ~0.01m precision
        mid = (lo + hi) / 2.0
        prop = AcousticPropagation(
            leak_spl_at_1m=leak_spl_at_1m,
            distance_meters=mid,
            center_frequency_hz=sensor.center_frequency_hz,
            temp_c=temp_c,
            relative_humidity_pct=relative_humidity_pct,
        )
        result = check_ugld_trigger(prop, sensor)

        if result.triggered:
            best_range = mid
            lo = mid
        else:
            hi = mid

    return round(best_range, 2)


