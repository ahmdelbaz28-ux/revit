# ===========================================================================
# ISO 9613-1 Atmospheric Attenuation — Frequency-Dependent
# ===========================================================================
# Simplified lookup table for atmospheric attenuation coefficient alpha
# (dB/m) at 20°C, 50% relative humidity for common UGLD center frequencies.
#
# Source: ISO 9613-1:1993 §6, Table 2 (calculated values).
# These values represent PURE atmospheric absorption (molecular relaxation
# of oxygen and nitrogen). They do NOT include geometric spreading.
#
# At ultrasonic frequencies, attenuation increases DRAMATICALLY:
#   25 kHz  ->  ~0.12 dB/m  (long range, lower sensitivity)
#   40 kHz  ->  ~0.50 dB/m  (most common UGLD operating point)
#   55 kHz  ->  ~1.20 dB/m
#   80 kHz  ->  ~3.00 dB/m  (short range only)
#  100 kHz  ->  ~5.50 dB/m  (very short range, high attenuation)
#
# Temperature and humidity corrections are applied via the correction factor
# in atmospheric_attenuation_db_per_m().
_ISO_9613_ALPHA_20C_50RH: dict[float, float] = {
    # center_frequency_hz: alpha_dB_per_m at 20C, 50% RH
    20_000: 0.05,
    25_000: 0.12,
    31_500: 0.25,
    40_000: 0.50,
    50_000: 0.90,
    55_000: 1.20,
    63_000: 1.80,
    80_000: 3.00,
    100_000: 5.50,
}

# Default center frequency for UGLD calculations when not specified.
# 40 kHz is the most common operating frequency for commercial UGLD sensors
# (e.g., General Monitors Gassonic, MSA Senscient).
_DEFAULT_UGLD_FREQUENCY_HZ: float = 40_000.0

# SNR margin required for reliable detection.
# 6 dB = signal must be 4x the noise power (10^(6/10) = 3.98).
# This is a widely accepted minimum for industrial alarm systems.
# Reference: ISA-TR 84.00.07, IEC 61508 SIL determination.
_MIN_SNR_DB: float = 6.0


def atmospheric_attenuation_db_per_m(
    center_frequency_hz: float,
    temp_c: float = 20.0,
    relative_humidity_pct: float = 50.0,
) -> float:
    """Calculate atmospheric attenuation coefficient (dB/m) for ultrasonic frequencies.

    Uses ISO 9613-1:1993 §6 methodology with simplified lookup and correction.
    The base values are computed at 20°C / 50% RH, then temperature and humidity
    corrections are applied.

    At ultrasonic frequencies (25-100 kHz), atmospheric absorption dominates
    over geometric spreading beyond ~10-20m. Using a flat coefficient
    (e.g., 0.5 dB/m) is physically incorrect because:
      - At 25 kHz: actual alpha ≈ 0.12 dB/m (0.5 is 4x too conservative)
      - At 100 kHz: actual alpha ≈ 5.5 dB/m (0.5 is 11x too optimistic)

    The correction for temperature and humidity follows the trend from
    ISO 9613-1:1993 Annex E:
      - Higher temperature → slightly lower alpha (for ultrasonic range)
      - Higher humidity → lower alpha (more molecular relaxation at lower freq)
      - Lower humidity → higher alpha (less absorption by water vapor,
        but more by oxygen relaxation processes at ultrasonic frequencies)

    Args:
        center_frequency_hz: Center frequency of the ultrasonic signal (Hz).
            Typical UGLD: 25,000-100,000 Hz.
        temp_c: Ambient temperature in degrees Celsius.
        relative_humidity_pct: Relative humidity in percent (0-100).

    Returns:
        Atmospheric attenuation coefficient in dB/m. Always positive.

    Reference: ISO 9613-1:1993 §6, Table 2 + Annex E

    """
    # V65 SAFETY: Validate inputs before computation.
    # This is a standalone function (not protected by Pydantic model validation).
    # NaN/Inf inputs produce NaN alpha → NaN SPL → false compliance pass.
    if not math.isfinite(center_frequency_hz) or center_frequency_hz <= 0:
        raise ValueError(
            f"center_frequency_hz must be positive and finite, got {center_frequency_hz}. "
            f"Non-positive or NaN frequency makes atmospheric attenuation undefined."
        )
    if not math.isfinite(temp_c) or temp_c < -40.0 or temp_c > 85.0:
        raise ValueError(
            f"temp_c must be finite and in range [-40, 85]°C, got {temp_c}. "
            f"Outside this range, ISO 9613-1 corrections are unreliable."
        )
    if not math.isfinite(relative_humidity_pct) or relative_humidity_pct < 0.0 or relative_humidity_pct > 100.0:
        raise ValueError(
            f"relative_humidity_pct must be finite and in range [0, 100]%, got {relative_humidity_pct}."
        )

    # Find nearest reference frequency in the lookup table
    ref_freqs = sorted(_ISO_9613_ALPHA_20C_50RH.keys())
    nearest = min(ref_freqs, key=lambda f: abs(f - center_frequency_hz))
    base_alpha = _ISO_9613_ALPHA_20C_50RH[nearest]

    # If the requested frequency is between table entries, interpolate linearly
    if center_frequency_hz != nearest:
        # Find the two bracketing frequencies
        lower = max((f for f in ref_freqs if f <= center_frequency_hz), default=ref_freqs[0])
        upper = min((f for f in ref_freqs if f >= center_frequency_hz), default=ref_freqs[-1])

        if lower != upper:
            alpha_lower = _ISO_9613_ALPHA_20C_50RH[lower]
            alpha_upper = _ISO_9613_ALPHA_20C_50RH[upper]
            # Linear interpolation in log-frequency space
            log_frac = math.log10(center_frequency_hz / lower) / math.log10(upper / lower)
            base_alpha = alpha_lower + log_frac * (alpha_upper - alpha_lower)

    # Temperature correction: at ultrasonic frequencies, higher temperature
    # slightly reduces attenuation. Approximate correction from ISO 9613-1
    # Annex E data: ~-0.3% per degree C above 20°C.
    temp_correction = 1.0 - 0.003 * (temp_c - 20.0)
    temp_correction = max(0.7, min(temp_correction, 1.3))  # Clamp to physical range

    # Humidity correction: at ultrasonic frequencies, moderate humidity (40-60%)
    # produces the minimum attenuation. Very dry (<20%) or very humid (>80%)
    # conditions can increase attenuation. Simplified correction:
    #   - 20-80% RH: minimal correction (0.85-1.1)
    #   - <20%: increased attenuation (up to 1.5x)
    #   - >80%: slightly increased (up to 1.2x)
    if relative_humidity_pct < 20:
        hum_correction = 1.0 + 0.5 * (20.0 - relative_humidity_pct) / 20.0
    elif relative_humidity_pct > 80:
        hum_correction = 1.0 + 0.2 * (relative_humidity_pct - 80.0) / 20.0
    else:
        # 20-80% RH: slight dip around 50% (optimal), peak at edges
        hum_correction = 1.0 - 0.1 * abs(relative_humidity_pct - 50.0) / 30.0
        hum_correction = max(0.85, hum_correction)

    alpha = base_alpha * temp_correction * hum_correction
    return round(alpha, 4)


